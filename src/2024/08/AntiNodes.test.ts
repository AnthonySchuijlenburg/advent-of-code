import { describe, it, expect } from 'vitest';
import { partOne, partTwo } from './AntiNodes.js';

describe('AntiNodes:Part one', () => {
    it('example input 1', () => {
        const result = partOne([
            '..........',
            '..........',
            '..........',
            '....a.....',
            '..........',
            '.....a....',
            '..........',
            '..........',
            '..........',
            '..........',
        ]);
        expect(result).toBe(2);
    });

    it('example input 2', () => {
        const result = partOne([
            '..........',
            '..........',
            '..........',
            '....a.....',
            '........a.',
            '.....a....',
            '..........',
            '..........',
            '..........',
            '..........',
        ]);
        expect(result).toBe(4);
    });

    it('example input 3', () => {
        const result = partOne([
            '............',
            '........0...',
            '.....0......',
            '.......0....',
            '....0.......',
            '......A.....',
            '............',
            '............',
            '........A...',
            '.........A..',
            '............',
            '............',
        ]);
        expect(result).toBe(14);
    });
});

